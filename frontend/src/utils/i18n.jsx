const translations = { en, hi, ta, bn, or };

const LanguageContext = createContext(null);

function getNestedValue(object, key) {
  return key.split(".").reduce((current, part) => current?.[part], object);
}

function interpolate(template, vars) {
  if (typeof template !== "string") return template;
  if (!vars) return template;

  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => {
    const value = vars[key];
    return value == null ? "" : String(value);
  });
}

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => localStorage.getItem(STORAGE_KEY) || "en");

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo(() => {
    const translate = (key, vars) => {
      const selected = getNestedValue(translations[language], key);
      if (selected != null) return interpolate(selected, vars);
      return interpolate(getNestedValue(translations.en, key) ?? key, vars);
    };

    return {
      language,
      setLanguage,
      languages: supportedLanguages,
      t: translate,
    };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}
